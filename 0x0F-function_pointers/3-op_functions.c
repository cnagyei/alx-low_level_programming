#include "3-calc.h"

/**
 * op_add - return the sum of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: int sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the difference of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: int difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: int product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the division of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: int division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder of the division of two numbers
 * @a: a number
 * @b: a number
 *
 * Return: int remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

